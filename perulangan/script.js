console.log("For");
for (let angka = 1; angka <= 5; angka++) {
    console.log(angka);
}


console.log("While");
let angka1 = 5;
while (angka1 >= 1) {
    console.log(angka1);
    angka1 --;
}


console.log("Do while");
let angka2 = 7;
do {
    console.log(angka2);
    angka2 -= 2;
} while (angka2 >= 1)